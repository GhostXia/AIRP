test("parseCargoLockForChecksums: extracts name@version -> checksum", () => {
  const map = parseCargoLockForChecksums(path.join(FIXTURES, "Cargo.lock.sample"));
  assert.equal(map.get("adler2@2.0.1"), "320119579fcad9c21884f5c4861d16174d0e06250625266f50fe6898340abefa");
  assert.equal(map.get("tokio@1.52.3"), "ddd31a130427c27518df266943a5308ed92d4b226cc639f5a8f1002816174301");
  // Workspace members have no checksum (no `source` line, no `checksum`).
  assert.equal(map.get("airp-core@0.1.0"), null);
  // Packages with source but no checksum (rare) -> null.
  assert.equal(map.get("no-checksum-pkg@0.1.0"), null);
});

test("parseCargoLockForChecksums: missing file throws", () => {
  assert.throws(() => parseCargoLockForChecksums("/nonexistent/Cargo.lock"));
});

test("parseCargoLockForChecksums: non-package blocks do not pollute previous package", () => {
  // Cargo.lock v4 may contain [[patch.unused]], [[patch.<source>]], [[replace]],
  // [meta] or [metadata] blocks. These can carry `name = `, `version = `,
  // `checksum = ` lines that must NOT overwrite the previous [[package]]'s
  // fields. The parser must flush + null `cur` when entering any non-package
  // block.
  const tmp = path.join(FIXTURES, "..", "tmp-cargo-patch-blocks.test.lock");
  const cargoLockText = `# This file is automatically @generated by Cargo.
version = 4

[[package]]
name = "adler2"
version = "2.0.1"
source = "registry+https://github.com/rust-lang/crates.io-index"
checksum = "320119579fcad9c21884f5c4861d16174d0e06250625266f50fe6898340abefa"

[[patch.unused]]
name = "openssl-sys"
version = "0.9.100"
source = "registry+https://github.com/rust-lang/crates.io-index"
checksum = "deadbeefdeadbeefdeadbeefdeadbeefdeadbeefdeadbeefdeadbeefdeadbeef"

[[package]]
name = "tokio"
version = "1.52.3"
source = "registry+https://github.com/rust-lang/crates.io-index"
checksum = "ddd31a130427c27518df266943a5308ed92d4b226cc639f5a8f1002816174301"

[meta]
target-applies-to-host = true
`;
  fs.writeFileSync(tmp, cargoLockText);
  try {
    const map = parseCargoLockForChecksums(tmp);
    // adler2 and tokio must have their correct checksums.
    assert.equal(
      map.get("adler2@2.0.1"),
      "320119579fcad9c21884f5c4861d16174d0e06250625266f50fe6898340abefa",
    );
    assert.equal(
      map.get("tokio@1.52.3"),
      "ddd31a130427c27518df266943a5308ed92d4b226cc639f5a8f1002816174301",
    );
    // The [[patch.unused]] entry for openssl-sys must NOT be recorded as a
    // package checksum (it's a patch declaration, not an installed package).
    assert.equal(map.get("openssl-sys@0.9.100"), undefined);
    // The [meta] block has no name/version/checksum, but we make sure it
    // doesn't crash the parser or pollute any previous entry.
    assert.equal(map.size, 2);
  } finally {
    fs.unlinkSync(tmp);
  }
});

