test("atomicWrite: writes file and cleans up tmp on success", async () => {
  const target = path.join(FIXTURES, "..", "tmp-atomic-write.test.txt");
  try {
    atomicWrite(target, "hello");
    assert.equal(fs.readFileSync(target, "utf8"), "hello");
    // tmp file must not linger.
    assert.equal(fs.existsSync(`${target}.tmp`), false);
  } finally {
    try { fs.unlinkSync(target); } catch {}
  }
});

test("atomicWrite: does not leave partial file if rename target is invalid", () => {
  // Renaming into a non-existent directory throws. The tmp file should be
  // cleaned up and no final file created.
  const badTarget = path.join(FIXTURES, "..", "nonexistent-dir", "tmp-atomic-fail.test.txt");
  assert.throws(() => atomicWrite(badTarget, "hello"));
  assert.equal(fs.existsSync(badTarget), false);
  // tmp in the (also non-existent) dir won't exist either.
  assert.equal(fs.existsSync(`${badTarget}.tmp`), false);
});

