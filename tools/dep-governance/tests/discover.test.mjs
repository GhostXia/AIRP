test("renderInventoryText: includes attention section for block/audit-required", () => {
  const records = [
    { ecosystem: "cargo", name: "good", version: "1.0.0", audit_class: "auto-pass", audit_reason: "MIT", license_normalized: "MIT", scope: "runtime", tier: "third-party", resolved: null },
    { ecosystem: "cargo", name: "bad", version: "2.0.0", audit_class: "block", audit_reason: "GPL-3.0 in runtime", license_normalized: "GPL-3.0", scope: "runtime", tier: "third-party", resolved: "https://example.com" },
  ];
  const meta = {
    generated_at: "2026-07-18T00:00:00.000Z",
    repo_root: "/workspace",
    total_records: 2,
    summary: {
      by_audit_class: { "auto-pass": 1, block: 1 },
      by_ecosystem: { cargo: 2 },
    },
  };
  const text = renderInventoryText(records, meta);
  assert.match(text, /AIRP dependency inventory/);
  assert.match(text, /Records requiring attention/);
  assert.match(text, /\[block\] cargo\/bad@2\.0\.0/);
  assert.match(text, /GPL-3\.0 in runtime/);
});
