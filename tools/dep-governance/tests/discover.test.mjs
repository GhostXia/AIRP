test("computeCargoScopes: classifies workspace member and edges", () => {
  const scopes = computeCargoScopes(METADATA_FIXTURE);
  // Workspace member itself.
  assert.equal(scopes.get("airp-core 0.1.0 (path+file:///workspace/engine)"), "runtime");
  // Normal dep -> runtime.
  assert.equal(scopes.get("tokio 1.52.3 (registry+https://github.com/rust-lang/crates.io-index)"), "runtime");
  assert.equal(scopes.get("serde 1.0.210 (registry+https://github.com/rust-lang/crates.io-index)"), "runtime");
  // Build dep -> build.
  assert.equal(scopes.get("tauri-build 2.0.0 (registry+https://github.com/rust-lang/crates.io-index)"), "build");
  // Dev dep -> dev.
  assert.equal(scopes.get("wiremock 0.6.0 (registry+https://github.com/rust-lang/crates.io-index)"), "dev");
  // Transitive normal dep of runtime dep -> runtime.
  assert.equal(scopes.get("bytes 1.7.0 (registry+https://github.com/rust-lang/crates.io-index)"), "runtime");
});

test("buildCargoRecords: marks tiers and sources correctly", () => {
  const checksums = new Map([
    ["tokio@1.52.3", "ddd31a130427c27518df266943a5308ed92d4b226cc639f5a8f1002816174301"],
    ["bytes@1.7.0", null],
  ]);
  const records = buildCargoRecords(METADATA_FIXTURE, checksums, ["airp-core", "airp-state-protocol", "airp-ui"]);

  const airp = records.find((r) => r.name === "airp-core");
  assert.equal(airp.tier, "first-party");
  assert.equal(airp.source, "workspace");
  assert.equal(airp.resolved, null);

  const tokio = records.find((r) => r.name === "tokio");
  assert.equal(tokio.tier, "third-party");
  assert.equal(tokio.source, "crates.io");
  assert.equal(tokio.integrity, "sha256:ddd31a130427c27518df266943a5308ed92d4b226cc639f5a8f1002816174301");
  assert.equal(tokio.scope, "runtime");
  assert.match(tokio.resolved, /^https:\/\/crates\.io\/api\/v1\/crates\/tokio\/1\.52\.3\/download$/);

  // bytes has no checksum in our fixture map -> integrity null.
  const bytes = records.find((r) => r.name === "bytes");
  assert.equal(bytes.integrity, null);
});

